    private void loadProductsToTable() {

    DefaultTableModel model =
            (DefaultTableModel) productTable.getModel();

    model.setRowCount(0); // clear table

    for (Product p : DataStore.products) {
        model.addRow(new Object[]{
            p.getProductName(),
            p.getCategory(),
            p.getPrice(),
            p.getStock(),
            p.getStatus()
        });
    }
}


