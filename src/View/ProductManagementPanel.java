    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
try {
        String name = productNameField.getText().trim();
        String category = categoryCombo.getSelectedItem().toString();
        String status = statusCombo.getSelectedItem().toString();

        double price = Double.parseDouble(priceField.getText().trim());
        int stock = Integer.parseInt(stockField.getText().trim());

        // Empty check
        if (name.isEmpty()) {
            throw new IllegalArgumentException("Product name is required");
        }

        // Alphabet-only name
        if (!name.matches("[a-zA-Z ]+")) {
            throw new IllegalArgumentException("Product name cannot contain numbers");
        }

        // Numeric validation
        if (price <= 0) {
            throw new IllegalArgumentException("Price must be greater than 0");
        }

        if (stock < 0) {
            throw new IllegalArgumentException("Stock cannot be negative");
        }

        // Create object
        Product product = new Product(name, category, price, stock, status);
        productList.add(product);

        // Add to table
        DefaultTableModel model =
                (DefaultTableModel) productTable.getModel();

        model.addRow(new Object[]{
            productList.size(),
            product.getProductName(),
            product.getCategory(),
            product.getPrice(),
            product.getStock(),
            product.getStatus()
        });

        // Clear form
        productNameField.setText("");
        priceField.setText("");
        stockField.setText("");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
                "Price and stock must be numeric",
                "Input Error",
                JOptionPane.ERROR_MESSAGE);

    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this,
                e.getMessage(),
                "Input Error",
                JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
int row = productTable.getSelectedRow();

    if (row == -1) {
        JOptionPane.showMessageDialog(this,
                "Please select a product to delete",
                "Delete Error",
                JOptionPane.WARNING_MESSAGE);
        return;
    }

    int modelRow = productTable.convertRowIndexToModel(row);

    productList.remove(modelRow);

    DefaultTableModel model =
            (DefaultTableModel) productTable.getModel();
    model.removeRow(modelRow);
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
 try {
        int row = productTable.getSelectedRow();
        if (row == -1) {
            throw new IllegalArgumentException("Select a product to update");
        }

        int modelRow = productTable.convertRowIndexToModel(row);

        String name = productNameField.getText().trim();
        String category = categoryCombo.getSelectedItem().toString();
        String status = statusCombo.getSelectedItem().toString();

        double price = Double.parseDouble(priceField.getText().trim());
        int stock = Integer.parseInt(stockField.getText().trim());

        if (!name.matches("[a-zA-Z ]+")) {
            throw new IllegalArgumentException("Invalid product name");
        }

        if (price <= 0 || stock < 0) {
            throw new IllegalArgumentException("Invalid price or stock value");
        }

        // Update object
        Product product = productList.get(modelRow);
        product.setProductName(name);
        product.setCategory(category);
        product.setPrice(price);
        product.setStock(stock);
        product.setStatus(status);

        // Update table
        DefaultTableModel model =
                (DefaultTableModel) productTable.getModel();

        model.setValueAt(product.getProductName(), modelRow, 0);
        model.setValueAt(product.getCategory(), modelRow, 1);
        model.setValueAt(product.getPrice(), modelRow, 2);
        model.setValueAt(product.getStock(), modelRow, 3);
        model.setValueAt(product.getStatus(), modelRow, 4);

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this,
                "Price and stock must be numeric",
                "Update Error",
                JOptionPane.ERROR_MESSAGE);

    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this,
                e.getMessage(),
                "Update Error",
                JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

